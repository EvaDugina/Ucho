CATEGORY_BY_KEY = {c.key: c for c in CATEGORIES}
THEMES_BY_CATEGORY = {c.key: set(c.themes) for c in CATEGORIES}


def all_categories() -> tuple[str, ...]:
    return tuple(c.key for c in CATEGORIES)


def all_themes() -> Iterable[tuple[str, str]]:
    for category in CATEGORIES:
        for theme in category.themes:
            yield category.key, theme


def is_valid_category(category: str | None) -> bool:
    return bool(category and category in CATEGORY_BY_KEY)


def is_valid_theme(category: str | None, theme: str | None) -> bool:
    if not category or not theme:
        return False
    return theme in THEMES_BY_CATEGORY.get(category, set())
